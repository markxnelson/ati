package com.samuiinteractive.ati;

public class BookmarkPojo {
	
	private String title;
	private String url;
	
	public BookmarkPojo() {}
		
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getUrl() {
		return url;
	}
	public void setUrl(String url) {
		this.url = url;
	}

}
